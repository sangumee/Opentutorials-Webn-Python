import os.path

# Number
print(1)  # Integer

# String
print('Hello Python')

# Boolean
print(True)
print(False)

# Expression
print(1+1)
print('Hello '+'Python')  # Hello Python

# Comparision Operator
print(1 == 1)  # True
print(1 == 2)  # False
print(1 > 2)  # False

# Membership Operator
print('world' in 'Hello world')  # check string exists

# check file exists
print(os.path.exists('boolean.py'))
